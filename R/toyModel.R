toyModel <-
function (model = "CP") 
{
    if (model == "CP") {
        X <- rand_tensor(c(30, 30, 30))
        X <- X * X
        X[1:5, 1:5, 1:5] <- X[1:5, 1:5, 1:5] * 1000
        X[10:14, 10:14, 10:14] <- X[10:14, 10:14, 10:14] * 1000
        X[18:22, 18:22, 18:22] <- X[18:22, 18:22, 18:22] * 1000
        X[26:30, 26:30, 26:30] <- X[26:30, 26:30, 26:30] * 1000
    }
    else if (model == "Tucker") {
        X <- rand_tensor(c(50, 50, 50))
        X <- X * X
        X[1:5, 1:5, 1:50] <- X[1:5, 1:5, 1:50] * 100
        X[46:50, 46:50, 1:50] <- X[46:50, 46:50, 1:50] * 100
        X[1:50, 16:20, 1:5] <- X[1:50, 16:20, 1:5] * 100
        X[1:50, 30:35, 46:50] <- X[1:50, 30:35, 46:50] * 100
        X[10:15, 1:50, 16:20] <- X[10:15, 1:50, 16:20] * 100
        X[30:35, 1:50, 30:35] <- X[30:35, 1:50, 30:35] * 100
    }
    else if (model == "NMF") {
        X <- matrix(runif(100 * 300), nrow = 100, ncol = 300)
        X <- X * X
        X[1:50, 1:50] <- X[1:50, 1:50] * 100
        X[50:70, 51:100] <- X[50:70, 51:100] * 200
        X[60:65, 151:170] <- X[60:65, 151:170] * 150
        X[25:35, 200:220] <- X[25:35, 200:220] * 150
        X[51:100, 220:300] <- X[51:100, 220:300] * 150
    }
    else {
        stop("Please specify model parameter as CP, Tucker, or NMF")
    }
    X
}
